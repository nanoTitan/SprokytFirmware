void HW_PwmTIM1SetCompare(int channel, int compare)
{
	if (channel < TIM_CHANNEL_1 || channel > TIM_CHANNEL_4)
		return;

	__HAL_TIM_SET_COMPARE(&htim1, channel, compare);
}

void HW_PwmTIM3SetCompare(int channel, int compare)
{
	if (channel < TIM_CHANNEL_1 || channel > TIM_CHANNEL_4)
		return;

	__HAL_TIM_SET_COMPARE(&htim3, channel, compare);
}

void HW_PWMSetFrequency(int timer, uint16_t frequency)
{
	TIM_HandleTypeDef* pTim = NULL;
	switch (timer)
	{
	case TIMER_3:
		pTim = &htim3;
		break;
		
	default:
		return;
	}
	
	uint32_t autoReload = (SystemCoreClock + frequency / 2) / frequency - 1;
	__HAL_TIM_SET_AUTORELOAD(pTim, autoReload);
}